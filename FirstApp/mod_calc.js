var soma = function (valor1, valor2){
    return valor1 + valor2;
}

var div = function (valor1, valor2){
    return valor1 / valor2;
}

var sub = function (valor1, valor2){
    return valor1 - valor2;
}

var mul = function (valor1, valor2){
    return valor1 * valor2;
}

module.exports = { 
    soma,
    sub,
    div,
    mul
}